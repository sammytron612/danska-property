            <span id="hs_cos_wrapper_post_body" class="mt-5" data-hs-cos-type="rich_text"><p>The value of the pound has tumbled following Chancellor Kwasi Kwarteng’s mini-Budget. We take a look at what it’s likely to mean for mortgage rates.</p>
